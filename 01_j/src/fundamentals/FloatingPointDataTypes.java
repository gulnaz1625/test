package fundamentals;

public class FloatingPointDataTypes {

    public static void main(String[]ap){

       // float   f   =    5.5    ;

        float temperature = 80.5f ;
        System.out.println(temperature);
        temperature = 75.6F;
        System.out.println(temperature);

        double d = 8945789.58978 ;
        System.out.println(d);
        d = 69.6d;
        System.out.println(d);
        d = 9.6D;
        System.out.println(d);

        double val = 56.5f ;



        /*
            Java is a strongly typed or Strictly typed language. Both, the value,
            and the variable to  which we want to assign the value , should have
            the same data type
        * */
    }

}
